export const getUniversityById = (id: string) =>
  universities.find((u) => u.id === id);

export const getUniversitiesByState = (state: string) =>
  universities.filter((u) => u.state === state);

export const getUniversitiesByType = (type: string) =>
  universities.filter((u) => u.type === type);

export const getTopRankedUniversities = (limit = 10) =>
  universities
    .filter((u) => u.ranking > 0)
    .sort((a, b) => a.ranking - b.ranking)
    .slice(0, limit);

export const getMostAffordable = (limit = 10) =>
  universities
    .sort((a, b) => a.tuitionInState - b.tuitionInState)
    .slice(0, limit);

export const usStates = [
  ...new Set(universities.map((u) => u.state).filter((s) => s !== "All States")),
].sort();
