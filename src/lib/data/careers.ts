export const getCareerById = (id: string) =>
  careers.find((c) => c.id === id);

export const getCareersByCategory = (category: string) =>
  careers.filter((c) => c.category === category);

export const getCareerCategories = () =>
  [...new Set(careers.map((c) => c.category))].sort();

export const getTopPayingCareers = (limit = 5) =>
  [...careers].sort((a, b) => b.avgSalary - a.avgSalary).slice(0, limit);

export const getFastestGrowingCareers = (limit = 5) =>
  [...careers].sort((a, b) => b.jobGrowth10yr - a.jobGrowth10yr).slice(0, limit);
